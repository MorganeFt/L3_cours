package io.github.MorganeFt.Seance12;

/***
 * Define a class Person with a name and a height. 
 ***/

public class Person {
	
	private String name;
	private int height;
	
	public Person(String name, int height) {
		this.name = name;
		this.height = height;
	}
	
	public String getName() {
		return name;
	}
	
	public int getHeight() {
		return height;
	}
	
}
